package com.s;

public class employee {
	 private String name;
	    private String position;

	    public employee(String name, String position) {
	        this.name = name;
	        this.position = position;
	    }

	    public String getName() {
	        return name;
	    }

	    public String getPosition() {
	        return position;
	    }

}
